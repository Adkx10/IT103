    private void viewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewButtonActionPerformed
        double baseSalary;
        double allowances;
        try {
            // Get the values entered by the user for employee number and month
            String empNo = employeeNo.getText();
            String month = hw.getText();

            // Initialize instances of 'Attendance' and 'Data' classes
            Attendance attend = new Attendance();
            Data data = new Data();
            StringBuilder salaryInfoText = new StringBuilder();
            
            // Set the employee number and month in the 'Attendance' object
            attend.SetEmployeeNo(empNo);
            attend.SetMonth(month);

            // Set the employee number in the 'Data' object
            data.SetEmployeeNo(empNo);

            // Compute the hours worked for the given employee and month
            attend.ComputeHourWorked();

            // Read employee Data to display
            data.ReadEmployee(empNo);

            allowances = data.RiceAllowance() + data.PhoneAllowance() + data.ClothAllowance();
            baseSalary = (data.HourlyRate() * attend.FinalHW()) + allowances;

            Deduct govt = new Deduct(baseSalary);
            // System out print conversion to display text from jTextArea
            ByteArrayOutputStream output = new ByteArrayOutputStream();
            PrintStream ps = new PrintStream(output);
            PrintStream old = System.out;
            System.setOut(ps);
            govt.Contributions();
            System.out.flush();
            System.setOut(old);
            String deductOutput = output.toString();

            DecimalFormat df = new DecimalFormat("#,##0.00");

            if (data.EmployeeFN() == null) {
                salaryInfoText.append("\n\t*** Employee Not Found! ***").append("\n");
            } else if (attend.FinalHW() != 0) {

                // Construct the salary information
                salaryInfoText.append("Employee No.: ").append(data.EmployeeNo()).append("\n");
                salaryInfoText.append("Employee Name: ").append(data.EmployeeFN()).append(" ").append(data.EmployeeLN()).append("\n");
                salaryInfoText.append("Month: ").append(attend.Month()).append("\n");
                salaryInfoText.append("Hours Worked: ").append(attend.FinalHW()).append("\n");
                salaryInfoText.append("Hourly Rare: ").append(data.HourlyRate()).append(" ").append("PHP").append("\n");
                salaryInfoText.append("\n------------------ Allowances ------------------").append("\n");
                salaryInfoText.append("Rice Subsidy: ").append(data.RiceAllowance()).append("\n");
                salaryInfoText.append("Phone: ").append(data.PhoneAllowance()).append("\n");
                salaryInfoText.append("Cloth: ").append(data.ClothAllowance()).append("\n");
                salaryInfoText.append("Gross Salary: ").append(df.format(baseSalary)).append("\n");
                salaryInfoText.append(" ").append(deductOutput);
                // Add other salary-related information as needed

            } else {
                salaryInfoText.append("\n\t*** No Record For This Month! ***").append("\n");
            }

            // Set the constructed salary information in the text panel
            showData.setText(salaryInfoText.toString());
        } catch (CsvValidationException | IOException | ParseException ex) {
            ex.printStackTrace(); // Handle exceptions properly in your application
        }

    }//GEN-LAST:event_viewButtonActionPerformed

