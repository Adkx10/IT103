    private void viewEmpDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewEmpDataActionPerformed
        try {
            // Get the value entered by the user for employee number
            String empNo = employeeNo.getText();

            // Initialize instance of 'Data' class
            Data data = new Data();
            StringBuilder infoText = new StringBuilder();

            // Set the employee number in the 'Data' object
            data.SetEmployeeNo(empNo);

            // Read employee Data to display
            data.ReadEmployee(empNo);

            // To print strings on the text field showData
            ByteArrayOutputStream output = new ByteArrayOutputStream();
            PrintStream ps = new PrintStream(output);
            PrintStream old = System.out;
            System.setOut(ps);
            System.out.flush();
            System.setOut(old);

            // Catch an invalid input of employee number
            if (data.EmployeeNo() == null) {
                infoText.append("\n\t*** Employee Not Found! ***").append("\n");
            } else {
                // Construct the salary information
                infoText.append("Employee No.: ").append(data.EmployeeNo()).append("\n\n");
                infoText.append("Employee Name: ").append(data.EmployeeFN()).append(" ").append(data.EmployeeLN()).append("\n\n");
                infoText.append("Date of Birth: ").append(data.EmployeeDOB()).append("\n\n");
                infoText.append("Address: ").append(data.EmployeeAddress()).append("\n\n");
                infoText.append("Phone #: ").append(data.EmployeePhoneNumber()).append("\n\n");
                infoText.append("SSS #: ").append(data.EmployeeSSS()).append("\n\n");
                infoText.append("PhilHealth #: ").append(data.EmployeePhilHealth()).append("\n\n");
                infoText.append("TIN: ").append(data.EmployeeTIN()).append("\n\n");
                infoText.append("Pag-Ibig #: ").append(data.EmployeePagIbig()).append("\n\n");
                infoText.append("Employment Status: ").append(data.EmployeeStatus()).append("\n\n");
                infoText.append("Company Position: ").append(data.EmployeePosition()).append("\n\n");
                infoText.append("Immediate Supervisor: ").append(data.EmployeeSupervisor()).append("\n\n"); 
            }
            showData.setText(infoText.toString());
        } catch (CsvValidationException | IOException  ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_viewEmpDataActionPerformed

